##### DIRETÓRIO DO PROJETO #####

node_modules
# é a pasta que guarda todos os arquivos baixados das dependências instaladas;


##### ARQUIVOS DO PROJETO #####

package.json
# responsável por armazenar e descrever diversas informações de um projeto, como as versões do node e do npm utilizadas, a URL do repositório, a versão do projeto e as dependências de produção e de desenvolvimento;

package-lock.json
# é um arquivo que faz parte do ecossistema do Node.js e é usado para manter um registro explícito e preciso das versões exatas das dependências do seu projeto.