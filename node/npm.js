// NPM: Node Package Manager (gerenciador de pacotes do Node.js)

/* NPM CLI: ferramenta oficial que nos auxilia no gerenciamento de pacotes, sejam
eles dependências da nossa aplicação ou nossos próprios pacotes. É por meio
dele que criamos um projeto, instalamos e removemos pacotes, publicamos e
gerenciamos versões dos nossos próprios pacotes. */

/* ### ORGANIZAR!!!
COMANDOS NPM:
- npm init: é utilizado para inicializar um projeto node.js.
- npm init -y: é utilizado para inicializar um projeto node.js com todas as configurações padrão.
- npm run <scriptname>: é usado para executar scripts definidos no arquivo package.json de um projeto node.js.
- npm start: por padrão, o script "start" geralmente inicia a aplicação web, servidor ou serviço definido no projeto.

- npm install: instalar todas as dependências de um projeto que estão listadas no arquivo package.json. (npm i)
- npm install <packagename>: baixa o pacote do registro do NPM e o adiciona ao objeto dependencies do package.json.
- npm install <packagename> --save-dev: baixa o pacote do registro do NPM e o adiciona ao objeto devDependencies do package.json.
- npm install <packagename>@<version> --save-exact: instala uma versão específica do pacote, sem permitir atualizações futuras.
*/


/*
ATUALIZAÇÕES DE PACOTES:
^ - permite atualizações de versão minor e patch (por padrão).
~ - permite apenas atualizações de versão patch.

*/