/*

=====> INFERÊNCIA DE TIPO:
é capaz de deduzir automaticamente o tipo de uma variável com base no valor atribuído a ela, sem que o
programador precise explicitamente declarar o tipo.



=====> COMPILADOR:
traduz um código escrito em uma linguagem de alto nível (mais próxima da humana) para um código equivalente
escrito em uma linguagem de baixo nível (mais próxima da linguagem da máquina). Depois dessa tradução, o
código pode ser executado.

=====> TRANSPILADOR:
traduz um código escrito em uma linguagem de alto nível para um código equivalente em outra
linguagem de alto nível ou até em uma versão diferente da mesma linguagem.

=====> TSC (TYPESCRIPT COMPILER):
compilar o código TS em JS, fazendo as verificações de tipo no meio tempo.
$ npm init -y
$ npm i --save-dev typescript@4.4
$ npx tsc hello.ts

=====> tsconfig:
é o arquivo de configuração de um projeto em TypeScript.

=====> TS NODE:
compila o código TypeScript em JavaScript e executá usando o Node com um único comando.
faz esse processo sem gerar um arquivo .js para cada arquivo .ts da aplicação.
$ npx ts-node hello.ts



=====> TIPOS PRIMITIVOS TYPESCRIPT:
string, number, boolean, null, undefined;

=====> TIPO "ANY":
ao utilizá-lo, você afirma “eu não sei qual é o tipo desse valor e não me importo com isso”.

=====> TIPO UNKNOW:
um tipo que pode ser qualquer valor. É semelhante ao tipo any, porém mais seguro.




=====> TYPE ANNOTATION:
variáveis, funções (parâmetros, retorno de funções), arrays, objetos.

=====> TYPE ALIASES:
cria-se separado;

=====> TYPES UNION:
string | number

*/