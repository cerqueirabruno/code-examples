
/*

  igualdade estrita: ===
  igualdade solta: ==
  igualdade: =

  diferente: !=
  estritamente diferente: !==

  maior que: >
  menor que: <

  maior ou igual á: >=
  menor ou igual á: <=

*/

// ================================================== //

// 'igualdade estrita' vs 'igualdade solta' //

console.log(5 === 5); // true
console.log('5' === 5); // false (string !== number)
console.log(5 === '5'); // false (number !== string)

