DQL: Data Query Language
  SELECT: Consulta de dados

DML: Data Manipulation Language
  INSERT: Inserção de dados
  UPDATE: Atualização de dados
  DELETE: Exclusão de dados

DDL: Data Definition Language
  CREATE: Criação de objetos
  ALTER: Alteração de objetos
  DROP: Exclusão de objetos

DCL: Data Control Language
  GRANT: Concessão de privilégios
  REVOKE: Revogação de privilégios

TCL: Transaction Control Language
  BEGIN: 
  COMMIT: Confirmação de transação
  ROLLBACK: Desfazimento de transação
  SAVEPOINT: Pontos de salvamento
