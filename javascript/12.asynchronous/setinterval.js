/*

SET INTERVAL:
execute uma determinada função ou código repetidamente em intervalos de tempo especificados.

*/

setInterval(() => {
    console.log("this is the first message");
}, 5000);

setInterval(() => {
    console.log("this is the second message");
}, 3000);

setInterval(() => {
    console.log("this is the third message");
}, 1000);