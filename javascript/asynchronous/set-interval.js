// setInterval: uma função embutida em JavaScript que permite que você execute uma determinada função ou código repetidamente em intervalos de tempo especificados. Essa função é comumente usada para criar temporizadores, atualizar conteúdo dinamicamente em uma página da web, ou executar tarefas em segundo plano em intervalos regulares.

setInterval(() => {
    console.log("this is the first message");
}, 5000);

setInterval(() => {
    console.log("this is the second message");
}, 3000);

setInterval(() => {
    console.log("this is the third message");
}, 1000);

