// Math:
// O objeto Math fornece funções matemáticas e constantes que podem ser usadas para realizar várias operações matemáticas. Algumas das funções e constantes disponíveis no objeto Math incluem:

// Math.abs(): Retorna o valor absoluto de um número.
// Math.ceil(): Arredonda para cima para o inteiro mais próximo.
// Math.floor(): Arredonda para baixo para o inteiro mais próximo.
// Math.round(): Arredonda para o inteiro mais próximo.
// Math.random(): Retorna um número pseudoaleatório entre 0 (inclusive) e 1 (exclusive).
// Math.max() e Math.min(): Retorna o valor máximo ou mínimo de uma lista de argumentos.
// Math.PI: Representa o valor de π (pi).
// Exemplo de uso do objeto Math:

let absValue = Math.abs(-5); // Retorna o valor absoluto de -5
console.log(absValue); // Imprime 5

let randomNum = Math.random(); // Retorna um número aleatório entre 0 e 1
console.log(randomNum);
