/*

MATH

- Math.round(): arredonda um número para o inteiro mais próximo.
- Math.floor(): arredonda um número para baixo para o inteiro mais próximo.
- Math.ceil(): arredonda um número para cima para o inteiro mais próximo.
- Math.max(): retorna o maior valor entre os números passados como argumento.
- Math.min(): retorna o menor valor entre os números passados como argumento.
- Math.pow(): retorna a base elevada ao expoente.
- Math.sqrt(): retorna a raiz quadrada de um número.
- Math.random(): retorna um número aleatório entre 0 e 1.
- Math.abs(): retorna o valor absoluto de um número.
- Math.trunc(): retorna a parte inteira de um número, removendo os decimais.
- Math.sign(): retorna o sinal de um número, indicando se é positivo, negativo ou zero.

*/

// exemplo 1 (Math.round())
console.log(Math.round(4.7)); // 5
console.log(Math.round(4.4)); // 4

// exemplo 2 (Math.floor())
console.log(Math.floor(4.7)); // 4

// exemplo 3 (Math.ceil())
console.log(Math.ceil(4.3)); // 5

// exemplo 4 (Math.max())
console.log(Math.max(10, 20, 30)); // 30

// exemplo 5 (Math.min())
console.log(Math.min(10, 20, 30)); // 10

// exemplo 6 (Math.pow())
console.log(Math.pow(2, 3)); // 8

// exemplo 7 (Math.sqrt())
console.log(Math.sqrt(16)); // 4

// exemplo 8 (Math.random())
console.log(Math.random()); // ex: 0.8453 (o valor será aleatório a cada execução)

// exemplo 9 (Math.abs())
console.log(Math.abs(-10)); // 10

// exemplo 10 (Math.trunc())
console.log(Math.trunc(4.9)); // 4

// exemplo 11 (Math.sign())
console.log(Math.sign(-10)); // -1
console.log(Math.sign(10));  // 1
console.log(Math.sign(0));   // 0

// exemplo 12 (Math.PI)
console.log(Math.PI); // 3.141592653589793
