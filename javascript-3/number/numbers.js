// O objeto Number é um objeto embutido em JavaScript que representa valores numéricos. Ele é usado para criar instâncias de números ou converter valores em números. Algumas propriedades e métodos importantes do objeto Number incluem:

// Number.parseFloat(): Converte uma string para um número de ponto flutuante.
// Number.parseInt(): Converte uma string para um número inteiro.
// Number.isNaN(): Verifica se um valor é NaN (Not-a-Number).
// Number.isInteger(): Verifica se um valor é um número inteiro.
// Number.MAX_VALUE e Number.MIN_VALUE: Representam os valores máximo e mínimo positivos que podem ser representados em JavaScript.
// Exemplo de uso do objeto Number:

let num = Number("123"); // Converte a string "123" para um número
console.log(num); // Imprime 123

